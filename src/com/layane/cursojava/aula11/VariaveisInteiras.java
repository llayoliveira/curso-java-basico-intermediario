package com.layane.cursojava.aula11;

public class VariaveisInteiras {

	public static void main(String[] args) {
		byte idade1 = 20;
		
		short idade2 = 21;
		
		int idade3 = 22;
		
		long idade4 = 23;
		
		System.out.println("Valor variável idade1 = " + idade1);
		System.out.println("Valor variável idade2 = " + idade2);
		System.out.println("Valor variável idade3 = " + idade3);
		System.out.println("Valor variável idade4 = " + idade4);
	}

}
